 preg_match_all($pattern, $name, $matches);
 if (!empty($matches))
 {
 $name = '';
 for($j = 0; $j < count($matches[0]); $j++)
 {
 $str = $matches[0][$j];
 if (strpos($str, '\\u') === 0)
 {
 $code = base_convert(substr($str, 2, 2), 16, 10);
 $code2 = base_convert(substr($str, 4), 16, 10);
 $c = chr($code).chr($code2);
 $c = iconv('UCS-2', 'UTF-8', $c);
 $name .= $c;
 }
 else
 {
 $name .= $str;
 }
 }
 }
 return $name;
 }
